import random
import string


def generate_username():
    adjective = random.choice(adjectives).capitalize()
    noun = random.choice(nouns).capitalize()
    suffix = ''.join(random.choices(string.digits, k=4))
    username = f'{adjective}{noun}#{suffix}'
    return username


